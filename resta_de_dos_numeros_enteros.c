#include <stdio.h>

int main(int argc, char *argv[]) {
	int numero1, numero2,resta;	
	printf ("Ingresa el primer nunero entero\n");
	scanf ("%d",&numero1);
	
	printf ("Ingresa el segundo nunero entero\n");
	scanf ("%d",&numero2);
	
	resta = numero1 - numero2;
	printf ("El resultado de la resta es %d",resta);
	
	return 0;
}

